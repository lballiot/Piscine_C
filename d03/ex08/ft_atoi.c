int		ft_atoi(char *str)
{
	int i;
	int neg;
	int var;

	var = 0;
	i = 0;
	neg = 1;
	while (((str[i] >= 8) && (str[i] <= 13)) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		neg = -1;
	if ((str[i] == '+') || (str[i] == '-'))
		i++;
	while (str[i] <= '9' && str[i] >= '0')
	{
		var = var * 10 + (str[i] - '0');
		i++;
	}
	return (var * neg);
}
