char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	while (str[i] != '\0')
	{
		if ((str[i - 1] >= 'a' && str[i - 1] <= 'z') || (str[i - 1] >= 'A' &&
				str[i - 1] <= 'Z') || (str[i - 1] >= '0' && str[i - 1] <= '9'))
			i++;
		else
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] -= 32;
			i++;
		}
		if (str[i] >= 'A' && str[i] <= 'Z')
			if ((str[i - 1] >= 'a' && str[i - 1] <= 'z')
					|| (str[i - 1] >= 'A' && str[i - 1] <= 'Z')
					|| (str[i - 1] >= '0' && str[i - 1] <= '9'))
				str[i] += 32;
	}
	str[i] = '\0';
	return (str);
}
