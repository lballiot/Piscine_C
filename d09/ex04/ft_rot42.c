char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (((str[i] >= 'A') && (str[i] <= 'Z')) || ((str[i] >= 'a') &&
				(str[i] <= 'z')))
		{
			if (str[i] >= 'k')
				str[i] -= 10;
			else if (str[i] >= 'K' && str[i] < 'a')
				str[i] -= 10;
			else
				str[i] += 16;
		}
		i++;
	}
	return (str);
}
